#!/usr/bin/env node

/**
 * VERIFICACI√ìN INTEGRAL DEL SISTEMA EDUCATIVO PAES
 * Paso 6: Verificaci√≥n de la integridad del sistema educativo
 * 
 * Este script verifica todas las funcionalidades educativas:
 * - Context7
 * - Sistema cu√°ntico educativo
 * - Triada renacentista aplicativa
 * - Multimodalidad Gemini
 * - Plataforma PAES
 */

import fs from 'fs';
import path from 'path';

class VerificadorSistemaEducativo {
    constructor() {
        this.resultados = {
            context7: false,
            sistemaQuantico: false,
            triadaRenacentista: false,
            multimodalidadGemini: false,
            plataformaPAES: false,
            integridad: false
        };
        
        this.errores = [];
        this.advertencias = [];
        this.exitos = [];
    }

    /**
     * 1. VERIFICACI√ìN CONTEXT7
     */
    async verificarContext7() {
        console.log('\nüîç VERIFICANDO CONTEXT7...');
        
        try {
            // Verificar configuraci√≥n educativa
            const configPath = 'config/educational.json';
            if (fs.existsSync(configPath)) {
                const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
                
                // Verificar estructura PAES oficial
                const subjectosRequeridos = [
                    'MATEMATICA_M1',
                    'MATEMATICA_M2', 
                    'COMPETENCIA_LECTORA',
                    'CIENCIAS',
                    'HISTORIA'
                ];
                
                const subjectosPresentes = config.educational?.subjects || [];
                const todosPresentes = subjectosRequeridos.every(s => 
                    subjectosPresentes.includes(s)
                );
                
                if (todosPresentes) {
                    this.exitos.push('‚úÖ Context7: Estructura PAES oficial completa');
                    this.resultados.context7 = true;
                } else {
                    this.errores.push('‚ùå Context7: Faltan asignaturas PAES');
                }
                
                // Verificar niveles Bloom
                if (config.educational?.bloom_levels === 6) {
                    this.exitos.push('‚úÖ Context7: Taxonom√≠a Bloom completa (6 niveles)');
                } else {
                    this.advertencias.push('‚ö†Ô∏è  Context7: Niveles Bloom incompletos');
                }
                
            } else {
                this.errores.push('‚ùå Context7: Archivo de configuraci√≥n educativa no encontrado');
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Context7: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * 2. VERIFICACI√ìN SISTEMA CU√ÅNTICO EDUCATIVO
     */
    async verificarSistemaQuantico() {
        console.log('\nüîÆ VERIFICANDO SISTEMA CU√ÅNTICO EDUCATIVO...');
        
        try {
            const configPath = 'config/educational.json';
            if (fs.existsSync(configPath)) {
                const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
                
                // Verificar motor cu√°ntico educativo
                if (config.features?.quantum_engine === true) {
                    this.exitos.push('‚úÖ Sistema Cu√°ntico: Motor cu√°ntico educativo activado');
                    
                    // Verificar nodos de aprendizaje neural
                    if (config.educational?.neural_learning === 'enabled') {
                        this.exitos.push('‚úÖ Sistema Cu√°ntico: Aprendizaje neural habilitado');
                        this.resultados.sistemaQuantico = true;
                    } else {
                        this.advertencias.push('‚ö†Ô∏è  Sistema Cu√°ntico: Aprendizaje neural no configurado');
                    }
                    
                } else {
                    this.errores.push('‚ùå Sistema Cu√°ntico: Motor cu√°ntico educativo desactivado');
                }
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Sistema Cu√°ntico: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * 3. VERIFICACI√ìN TRIADA RENACENTISTA APLICATIVA
     */
    async verificarTriadaRenacentista() {
        console.log('\nüé® VERIFICANDO TRIADA RENACENTISTA APLICATIVA...');
        
        try {
            const configPath = 'config/educational.json';
            if (fs.existsSync(configPath)) {
                const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
                
                // Verificar los tres pilares: Arte, Ciencia, Tecnolog√≠a
                const asignaturas = config.educational?.subjects || [];
                
                // Ciencia
                const tieneMatematicas = asignaturas.includes('MATEMATICA_M1') && 
                                       asignaturas.includes('MATEMATICA_M2');
                const tieneCiencias = asignaturas.includes('CIENCIAS');
                
                // Arte/Humanidades
                const tieneHistoria = asignaturas.includes('HISTORIA');
                const tieneCompetenciaLectora = asignaturas.includes('COMPETENCIA_LECTORA');
                
                // Tecnolog√≠a (integraci√≥n Spotify para aprendizaje)
                const tieneIntegracionTecnologica = config.integrations?.spotify?.enabled === true;
                
                if (tieneMatematicas && tieneCiencias) {
                    this.exitos.push('‚úÖ Triada Renacentista: Pilar Cient√≠fico completo');
                }
                
                if (tieneHistoria && tieneCompetenciaLectora) {
                    this.exitos.push('‚úÖ Triada Renacentista: Pilar Human√≠stico completo');
                }
                
                if (tieneIntegracionTecnologica) {
                    this.exitos.push('‚úÖ Triada Renacentista: Pilar Tecnol√≥gico habilitado');
                }
                
                if (tieneMatematicas && tieneCiencias && tieneHistoria && 
                    tieneCompetenciaLectora && tieneIntegracionTecnologica) {
                    this.resultados.triadaRenacentista = true;
                    this.exitos.push('‚úÖ Triada Renacentista: Integraci√≥n completa verificada');
                } else {
                    this.advertencias.push('‚ö†Ô∏è  Triada Renacentista: Integraci√≥n parcial');
                }
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Triada Renacentista: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * 4. VERIFICACI√ìN MULTIMODALIDAD GEMINI
     */
    async verificarMultimodalidadGemini() {
        console.log('\nü§ñ VERIFICANDO MULTIMODALIDAD GEMINI...');
        
        try {
            const configPath = 'config/educational.json';
            if (fs.existsSync(configPath)) {
                const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
                
                // Verificar OpenRouter para generaci√≥n de contenido
                if (config.integrations?.openrouter?.enabled === true) {
                    this.exitos.push('‚úÖ Multimodalidad Gemini: OpenRouter habilitado');
                    
                    if (config.integrations.openrouter.purpose === 'educational_content_generation') {
                        this.exitos.push('‚úÖ Multimodalidad Gemini: Generaci√≥n de contenido educativo configurada');
                        
                        // Verificar recomendaciones AI
                        if (config.features?.ai_recommendations === true) {
                            this.exitos.push('‚úÖ Multimodalidad Gemini: Recomendaciones AI activadas');
                            this.resultados.multimodalidadGemini = true;
                        } else {
                            this.advertencias.push('‚ö†Ô∏è  Multimodalidad Gemini: Recomendaciones AI desactivadas');
                        }
                        
                    } else {
                        this.errores.push('‚ùå Multimodalidad Gemini: Prop√≥sito no educativo');
                    }
                } else {
                    this.errores.push('‚ùå Multimodalidad Gemini: OpenRouter no configurado');
                }
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Multimodalidad Gemini: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * 5. VERIFICACI√ìN PLATAFORMA PAES
     */
    async verificarPlataformaPAES() {
        console.log('\nüìö VERIFICANDO PLATAFORMA PAES...');
        
        try {
            // Verificar package.json educativo
            const packagePath = 'package.json';
            if (fs.existsSync(packagePath)) {
                const packageJson = JSON.parse(fs.readFileSync(packagePath, 'utf8'));
                
                // Verificar nombre educativo
                if (packageJson.name === 'paes-educativo') {
                    this.exitos.push('‚úÖ Plataforma PAES: Configuraci√≥n educativa correcta');
                    
                    // Verificar keywords educativas
                    const keywordsEducativas = ['paes', 'education', 'bloom', 'mathematics'];
                    const tieneKeywordsEducativas = keywordsEducativas.every(k => 
                        packageJson.keywords?.includes(k)
                    );
                    
                    if (tieneKeywordsEducativas) {
                        this.exitos.push('‚úÖ Plataforma PAES: Keywords educativas presentes');
                    } else {
                        this.advertencias.push('‚ö†Ô∏è  Plataforma PAES: Keywords educativas incompletas');
                    }
                    
                    // Verificar dependencias educativas
                    const dependenciasRequeridas = ['@supabase/supabase-js', 'typescript', 'dotenv'];
                    const tieneDependencias = dependenciasRequeridas.every(d => 
                        packageJson.dependencies?.[d] || packageJson.devDependencies?.[d]
                    );
                    
                    if (tieneDependencias) {
                        this.exitos.push('‚úÖ Plataforma PAES: Dependencias educativas completas');
                        this.resultados.plataformaPAES = true;
                    } else {
                        this.errores.push('‚ùå Plataforma PAES: Dependencias faltantes');
                    }
                    
                } else {
                    this.errores.push('‚ùå Plataforma PAES: Configuraci√≥n no educativa detectada');
                }
            } else {
                this.errores.push('‚ùå Plataforma PAES: package.json no encontrado');
            }
            
            // Verificar configuraci√≥n Supabase educativa
            const configPath = 'config/educational.json';
            if (fs.existsSync(configPath)) {
                const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
                
                if (config.integrations?.supabase?.enabled === true && 
                    config.integrations.supabase.schema === 'educational') {
                    this.exitos.push('‚úÖ Plataforma PAES: Base de datos educativa configurada');
                } else {
                    this.advertencias.push('‚ö†Ô∏è  Plataforma PAES: Configuraci√≥n de base de datos incompleta');
                }
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Plataforma PAES: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * 6. VERIFICACI√ìN DE INTEGRIDAD GENERAL
     */
    async verificarIntegridad() {
        console.log('\nüîí VERIFICANDO INTEGRIDAD DEL SISTEMA...');
        
        try {
            // Verificar eliminaci√≥n de componentes de trading
            const componentesProhibidos = [
                'quantum-core',
                'quantum-ui', 
                'trading',
                'binance',
                'leonardo'
            ];
            
            let componentesEliminados = true;
            for (const componente of componentesProhibidos) {
                if (fs.existsSync(componente)) {
                    this.errores.push(`‚ùå Integridad: Componente de trading encontrado: ${componente}`);
                    componentesEliminados = false;
                }
            }
            
            if (componentesEliminados) {
                this.exitos.push('‚úÖ Integridad: Componentes de trading completamente eliminados');
            }
            
            // Verificar estructura educativa limpia
            const archivosEducativos = [
                'config/educational.json',
                'package.json',
                'README.md'
            ];
            
            let estructuraCompleta = true;
            for (const archivo of archivosEducativos) {
                if (!fs.existsSync(archivo)) {
                    this.errores.push(`‚ùå Integridad: Archivo educativo faltante: ${archivo}`);
                    estructuraCompleta = false;
                }
            }
            
            if (estructuraCompleta && componentesEliminados) {
                this.resultados.integridad = true;
                this.exitos.push('‚úÖ Integridad: Sistema educativo completamente √≠ntegro');
            }
            
        } catch (error) {
            this.errores.push(`‚ùå Integridad: Error en verificaci√≥n - ${error.message}`);
        }
    }

    /**
     * EJECUTAR TODAS LAS VERIFICACIONES
     */
    async ejecutarVerificacion() {
        console.log('üöÄ INICIANDO VERIFICACI√ìN INTEGRAL DEL SISTEMA EDUCATIVO PAES');
        console.log('=' * 80);
        
        await this.verificarContext7();
        await this.verificarSistemaQuantico();
        await this.verificarTriadaRenacentista();
        await this.verificarMultimodalidadGemini();
        await this.verificarPlataformaPAES();
        await this.verificarIntegridad();
        
        this.generarReporte();
    }

    /**
     * GENERAR REPORTE FINAL
     */
    generarReporte() {
        console.log('\n' + '=' * 80);
        console.log('üìä REPORTE FINAL DE VERIFICACI√ìN');
        console.log('=' * 80);
        
        // Mostrar √©xitos
        if (this.exitos.length > 0) {
            console.log('\n‚úÖ VERIFICACIONES EXITOSAS:');
            this.exitos.forEach(exito => console.log(`   ${exito}`));
        }
        
        // Mostrar advertencias
        if (this.advertencias.length > 0) {
            console.log('\n‚ö†Ô∏è  ADVERTENCIAS:');
            this.advertencias.forEach(advertencia => console.log(`   ${advertencia}`));
        }
        
        // Mostrar errores
        if (this.errores.length > 0) {
            console.log('\n‚ùå ERRORES ENCONTRADOS:');
            this.errores.forEach(error => console.log(`   ${error}`));
        }
        
        // Resumen de componentes
        console.log('\nüìà RESUMEN DE COMPONENTES:');
        console.log(`   Context7: ${this.resultados.context7 ? '‚úÖ FUNCIONAL' : '‚ùå FALLOS'}`);
        console.log(`   Sistema Cu√°ntico: ${this.resultados.sistemaQuantico ? '‚úÖ FUNCIONAL' : '‚ùå FALLOS'}`);
        console.log(`   Triada Renacentista: ${this.resultados.triadaRenacentista ? '‚úÖ FUNCIONAL' : '‚ùå FALLOS'}`);
        console.log(`   Multimodalidad Gemini: ${this.resultados.multimodalidadGemini ? '‚úÖ FUNCIONAL' : '‚ùå FALLOS'}`);
        console.log(`   Plataforma PAES: ${this.resultados.plataformaPAES ? '‚úÖ FUNCIONAL' : '‚ùå FALLOS'}`);
        console.log(`   Integridad General: ${this.resultados.integridad ? '‚úÖ √çNTEGRO' : '‚ùå COMPROMETIDO'}`);
        
        // Estado general
        const componentesFuncionales = Object.values(this.resultados).filter(Boolean).length;
        const totalComponentes = Object.keys(this.resultados).length;
        const porcentajeExito = (componentesFuncionales / totalComponentes * 100).toFixed(1);
        
        console.log('\nüéØ ESTADO GENERAL DEL SISTEMA:');
        console.log(`   Componentes funcionales: ${componentesFuncionales}/${totalComponentes}`);
        console.log(`   Porcentaje de √©xito: ${porcentajeExito}%`);
        
        if (porcentajeExito >= 80) {
            console.log('   üü¢ SISTEMA EDUCATIVO EN ESTADO √ìPTIMO');
        } else if (porcentajeExito >= 60) {
            console.log('   üü° SISTEMA EDUCATIVO REQUIERE ATENCI√ìN');
        } else {
            console.log('   üî¥ SISTEMA EDUCATIVO REQUIERE REPARACI√ìN URGENTE');
        }
        
        console.log('\n' + '=' * 80);
        console.log('‚úÖ VERIFICACI√ìN COMPLETADA');
        console.log('=' * 80);
    }
}

// Ejecutar verificaci√≥n
const verificador = new VerificadorSistemaEducativo();
verificador.ejecutarVerificacion().catch(console.error);
